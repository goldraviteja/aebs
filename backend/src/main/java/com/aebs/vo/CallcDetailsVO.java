package com.aebs.vo;

public class CallcDetailsVO {

    private String usoc;
    private int qty;
    private double cost;
    private double amount;
    private String occ;
    
	public String getUsoc() {
		return usoc;
	}
	public void setUsoc(String usoc) {
		this.usoc = usoc;
	}
	public int getQty() {
		return qty;
	}
	public void setQty(int qty) {
		this.qty = qty;
	}
	public double getCost() {
		return cost;
	}
	public void setCost(double cost) {
		this.cost = cost;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	public String getOcc() {
		return occ;
	}
	public void setOcc(String occ) {
		this.occ = occ;
	}
    
    
}
